    public function div_geojson()
    {
        $coordinates = array();
        $coordinate = array();
        $new_array = array();
        $division_array = array();

        for ($x = 1; $x <= 3; $x++) {
            $locations = DivisionGeojson::where('division_id', $x)->max('coordinate_code');
            $division_name = DivisionGeojson::with('division')->where('division_id', $x)->first();
            for ($i = 1; $i <= $locations; $i++) {
                $coordinates[$i] = DivisionGeojson::with('division')->where('coordinate_code', $i)->
                where('division_id', $x)->get();
                for ($j = 0; $j < count($coordinates[$i]); $j++) {
                    $coordinate[$j] = array((double)$coordinates[$i][$j]['lat'], (double)$coordinates[$i][$j]['long']);
                }
                $second_array = array($coordinate);
                $coordinate = array();
                array_push($new_array, $second_array);
            }
            $feature_data = array(
                'type' => 'Feature',
                'geometry' => array('type' => 'MultiPolygon', 'coordinates' => $new_array),
                'properties' => array('name' => $division_name->division->name),
                'id' => $division_name->division->id
            );
            array_push($division_array, $feature_data);
            $new_array = array();
        }

        $new_data = array(
            'type' => 'FeatureCollection',
            'features' => $division_array
        );

        return $new_data;
    }

